package chap4;

public class Switch_00 {
	public static void main(String[] args) {
		
		int num =8;
		switch(num) {
		case 8: System.out.println("안녕8");
		case 7 : System.out.println("안녕7");
		case 4 : System.out.println("안녕4");break;
		case 3 : System.out.println("안녕3");
		}
		
	}

}
